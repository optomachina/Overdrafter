import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(__dirname, "../src/integrations/supabase/types.ts");

const generated = execFileSync("supabase", ["gen", "types", "typescript", "--local"], {
  encoding: "utf8",
  stdio: ["ignore", "pipe", "inherit"],
});

const compatibilityAliases = `
export type AppRole = Enums<"app_role">
export type ClientOptionKind = Enums<"client_option_kind">
export type ExtractionStatus = Enums<"extraction_status">
export type JobFileKind = Enums<"job_file_kind">
export type JobStatus = Enums<"job_status">
export type ProjectInviteStatus = Enums<"project_invite_status">
export type ProjectRole = Enums<"project_role">
export type QueueTaskStatus = Enums<"queue_task_status">
export type QueueTaskType = Enums<"queue_task_type">
export type QuoteRequestStatus = Enums<"quote_request_status">
export type QuoteRunStatus = Enums<"quote_run_status">
export type VendorName = Enums<"vendor_name">
export type VendorStatus = Enums<"vendor_status">
`.trim();

const fileContents = [
  "// Generated by `npm run db:types` against local Supabase migration head.",
  "// Do not edit this file manually.",
  "",
  generated.trimEnd(),
  "",
  "// Compatibility aliases preserved for existing app imports.",
  compatibilityAliases,
  "",
].join("\n");

writeFileSync(outputPath, fileContents);
